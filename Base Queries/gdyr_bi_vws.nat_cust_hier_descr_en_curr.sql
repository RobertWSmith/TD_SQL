
SELECT
    C.SHIP_TO_CUST_ID
    , C.SHIP_TO_CUST_ID || ' - ' || C.CUST_NAME AS SHIP_TO_CUST

    , C.OWN_CUST_ID
    , C.OWN_CUST_ID || ' - ' || C.OWN_CUST_NAME AS OWN_CUST

    , C.CO_CD
    , C.CO_CD || ' - ' || C.CO_DESC AS CO

    , C.SALES_ORG_CD
    , C.SALES_ORG_CD || ' - ' || C.SALES_ORG_NAME AS SALES_ORG

    , C.DISTR_CHAN_CD
    , C.DISTR_CHAN_CD || ' - ' || C.DISTR_CHAN_NAME AS DISTR_CHAN

    , C.DIV_CD

    , C.CUST_GRP_ID
    , C.CUST_GRP_ID || ' - ' || C.CUST_GRP_NAME AS CUST_GRP

    , C.PRIM_SHIP_FACILITY_ID
    , C.DELIV_PRTY_CD
    , C.SHIP_COND_ID
    , C.CUST_GRP2_CD

    , C.CNTRY_NAME_CD
    , C.TERR_NAME
    , C.DISTRICT_NAME
    , C.CITY_NAME
    , C.POSTAL_CD

    , CAST(CASE
        WHEN C.SALES_ORG_CD IN ('N301', 'N311', 'N321', 'N307', 'N317', 'N336') OR (C.SALES_ORG_CD IN ('N303', 'N313', 'N323') AND C.DISTR_CHAN_CD IN('30', '31'))
            THEN 'Replacement'
        ELSE 'OE'
        END AS VARCHAR(25)) AS OE_REPL_IND

FROM GDYR_BI_VWS.NAT_CUST_HIER_DESCR_EN_CURR C

WHERE
    C.SHIP_TO_CUST_ID IN ('0000062021', '0000151303', '0000151712', '0000153793', '0000160448', '0000167310', '0002052203', '0002052235', '0002128647', '0002140466')

