    , DDC.ORDER_LINE_NBR
    
    , XR.FRT_MVMNT_ID
    , XR.FRT_MVMNT_SCHD_ID
    , XR.FRT_MVMNT_STOP_ID
    , XR.TM_DELIV_ID
    
    --, ROW_NUMBER() OVER (PARTITION BY DDC.ORDER_FISCAL_YR, DDC.ORDER_ID, DDC.ORDER_LINE_NBR ORDER BY DDC.DELIV_NOTE_CREA_DT ASC) AS DN_SEQ_ID
    
