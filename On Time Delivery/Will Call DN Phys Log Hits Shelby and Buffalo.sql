    , CUST.OWN_CUST_ID
    , CUST.OWN_CUST_NAME
    , CUST.TIRE_CUST_TYP_CD
    , POL.SHIP_FACILITY_ID AS SHIP_FACILITY_ID
    , FAC.FACILITY_NAME AS SHIP_FACILITY_NAME
    , POL.DELIV_PRTY_ID
    , POL.DELIV_BLK_IND
    , POL.CREDIT_HOLD_FLG
    , POL.NUM_DELIV_LINES
    , POL.CAN_REJ_REAS_ID
    , POL.PO_TYPE_ID
    , POL.CANCEL_IND
    , POL.CUST_GRP_ID
    , POL.CUST_GRP2_CD
    , POL.SHIP_COND_ID
    , POL.A_IND
    , POL.R_IND
    , POL.SPCL_PROC_ID
    , POL.MAX_CARR_SCAC_ID
    , POL.ORDER_DT

