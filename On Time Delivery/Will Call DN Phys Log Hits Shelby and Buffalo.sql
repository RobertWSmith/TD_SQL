    , SUM(ZEROIFNULL(Q.FCDD_PHYS_LOG_HIT_QTY)) AS FCDD_PHYS_LOG_HIT_QTY*/
    
    , Q.DELIV_NOTE_CREA_DT
    , Q.PLN_GOODS_MVT_DT
