    FM.FRT_MVMNT_ID
    , FM.FRT_MVMNT_SCHD_ID
    , FM.FRT_MVMNT_SCHD_ID
    
    , FM.FRT_MVMNT_STA_CD
    , FM.BILL_LADING_ID
    , FM.TRANSP_MODE_CD
    , FM.TM_CARR_ID
    , FM.TRLR_TYP_ID
    , FM.APPT_REQ_IND
    , FM.ALLCT_IND
    , FM.TRLR_ID
    , FM.TRLR_BLD_ID
    , FM.REF_BOL_ID
    , FM.RATE_TM_CARR_ID
    , FM.FRT_TERM_CD
    , FM.TEAM_IND
    , FM.XDOCK_CD
    
    , FM.FROM_TM_LOC_ID
    , FM.TO_TM_LOC_ID
    , FM.STOP_QTY
    , FM.WT_QTY
    , FM.CU_VOL_QTY
    , FM.PC_QTY
    , FM.PLLT_QTY
    , FM.NTFY_QTY
    , FM.MI_QTY
    
