    , CAST(FM.LATE_DEPART_DT AS TIMESTAMP(0)) + (FM.LATE_DEPART_TM - TIME '00:00:00' HOUR TO SECOND(0)) AS LATE_DEPART_TS
    , CAST(FM.BEST_DEPART_DT AS TIMESTAMP(0)) + (FM.BEST_DEPART_TM - TIME '00:00:00' HOUR TO SECOND(0)) AS BEST_DEPART_TS
